package com.mycompany.app.codegroup;

public class codeGroup {
	private Integer seq;
	private String abandonedDogClass;
	private String alarmCategory;
	private String breedCategory;
//	------
	
	public Integer getSeq() {
		return seq;
	}
	public void setSeq(Integer seq) {
		this.seq = seq;
	}
	public String getAbandonedDogClass() {
		return abandonedDogClass;
	}
	public void setAbandonedDogClass(String abandonedDogClass) {
		this.abandonedDogClass = abandonedDogClass;
	}
	public String getAlarmCategory() {
		return alarmCategory;
	}
	public void setAlarmCategory(String alarmCategory) {
		this.alarmCategory = alarmCategory;
	}
	public String getBreedCategory() {
		return breedCategory;
	}
	public void setBreedCategory(String breedCategory) {
		this.breedCategory = breedCategory;
	}
}
